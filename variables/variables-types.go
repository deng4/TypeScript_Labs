package main

import "fmt"

func main() {
	var number int
	number = 5
	fmt.Println(number)

	var text string
	text = "This is text"
	fmt.Println("This is string var: "+text)

	var intfloat float32
	intfloat=3.24
	fmt.Println("this is float:", intfloat)

	var mybool bool
	mybool=true
	fmt.Println("This is bool:", mybool)
}